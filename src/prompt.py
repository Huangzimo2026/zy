        "图书馆", "借书", "开放时间",
        "断电", "熄灯", "作息", "食堂", "校园卡", "饭卡", "校卡",
        "校区", "地址", "东校区", "大学路", "公交", "交通", "地铁",
        "请假", "补办", "证明", "在读证明", "军训",
        "辅导员", "学院", "保卫处", "校园110", "报警", "防骗", "冒充",
        "报销", "调课", "设备报修", "科研", "申报", "教室", "设备",
        "学校", "校园", "办事", "流程", "电话", "黄页", "卡"
